#include <iostream>
// find whether given number is divisible by 2 or not
int main()
{
    int a;
    cout<<"Enter a integer-";
    cin>>a;
    if (a%2==0)
        cout<<"YES,User Entered Number is Divisible by 2";
    else
        cout<<"NO,User Entered Number is Not Divisible by 2";
    return 0;
}
